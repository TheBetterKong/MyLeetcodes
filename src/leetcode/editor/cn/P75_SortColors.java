package leetcode.editor.cn;

import java.util.Arrays;

class P75_SortColors {
    public static void main(String[] args) {
        Solution solution = new P75_SortColors().new Solution();
        // TO TEST
        int[] nums = {2,0,2,1,1,0};
        solution.sortColors(nums);
        System.out.println(Arrays.toString(nums));
    }

    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public void sortColors(int[] nums) {
            int lenth = nums.length;
            if (lenth < 2) return;

            int left = 0, right = lenth - 1;
            for (int i = 0; i <= right; i++) {
                if (nums[i] == 0) {
                    nums[i] = nums[left];
                    nums[left] = 0;
                    left++;
                    // æŒ‡é’ˆ i æ˜¯æ¯” left å¿«çš„ï¼Œå› æ­¤ i ä¸ left ä¹‹é—´ä¸€å®šä¸å­˜åœ¨ 0 æˆ– 2
                }
                if (nums[i] == 2) {
                    nums[i] = nums[right];
                    nums[right] = 2;
                    right--;
                    // å› ä¸ºä¸çŸ¥é“åé¢æ¢è¿‡æ¥çš„æ•°æ˜¯å¤šå°‘ï¼Œå¦‚æœæ˜¯ 0 æˆ–è€… 2 çš„è¯ï¼Œéœ€è¦å›é€€å†åˆ¤æ–­
                    if (nums[i] != 1) {
                        i--;
                    }
                }
            }
        }
    }
    //leetcode submit region end(Prohibit modification and deletion)

}

