package leetcode.editor.cn;

class P13_RomanToInteger {
    public static void main(String[] args) {
        Solution solution = new P13_RomanToInteger().new Solution();
        // TO TEST
        String str = "MCMXCIV";
        int answer = solution.romanToInt(str);;
        System.out.println(answer);
    }

    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public int romanToInt(String s) {
            int preval = getValue(s.charAt(0));
            int sum = 0;
            for(int next = 1; next < s.length(); next++){
                int nextval = getValue(s.charAt(next));
                if(nextval > preval){
                    sum -= preval;
                }
                else
                    sum += preval;
                preval = nextval;
            }
            sum += preval;
            return sum;
        }

        private int getValue(char ch) {
            switch(ch) {
                case 'I': return 1;
                case 'V': return 5;
                case 'X': return 10;
                case 'L': return 50;
                case 'C': return 100;
                case 'D': return 500;
                case 'M': return 1000;
                default: return 0;
            }
        }
    }
    //leetcode submit region end(Prohibit modification and deletion)

}

